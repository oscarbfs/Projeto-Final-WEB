            <section id="introducao">
                <h2>Introdução</h2>
                <p>O FarmNote é um sistema de gerenciamento de gado criado para ajudar fazendeiros a manter o controle
                    de seus animais, pastos e fazendas de forma eficiente e organizada.</p>
            </section>

            <section id="problema">
                <h2>Problema</h2>
                <p>Os fazendeiros geralmente precisam lidar com uma grande quantidade de animais, pastos e fazendas, o
                    que pode ser difícil de gerenciar sem a ajuda de um sistema especializado. Isso pode levar a erros,
                    perda de informações importantes e prejuízos financeiros.</p>
            </section>

            <section id="justificativa">
                <h2>Justificativa</h2>
                <p>O FarmNote foi criado para ajudar os fazendeiros a superar os desafios de gerenciamento de gado,
                    permitindo que eles mantenham um controle mais eficiente e preciso de seus animais, pastos e
                    fazendas. Com o FarmNote, os fazendeiros podem economizar tempo e recursos, minimizar erros e
                    aumentar sua produtividade.</p>
            </section>

            <section id="objetivo">
                <h2>Objetivo</h2>
                <h3>Objetivo geral</h3>
                <p>O objetivo geral do FarmNote é fornecer aos fazendeiros uma ferramenta eficiente e organizada para o
                    gerenciamento de gado.</p>
                <h3>Objetivos específicos</h3>
                <ul>
                    <li>Permitir o cadastramento e o controle de bovinos, pastos e fazendas</li>
                    <li>Disponibilizar informações precisas sobre o estado de saúde e a localização dos animais, pastos
                        e fazendas</li>
                    <li>Facilitar o planejamento e a organização de atividades relacionadas ao gado, como alimentação,
                        vacinação e reprodução</li>
                    <li>Reduzir erros e perdas financeiras relacionadas ao gerenciamento de gado</li>
                </ul>
            </section>
            <section id="como-funciona">
                <h2>Como funciona</h2>
                <p>O FarmNote é um sistema baseado em banco de dados que permite o cadastramento e o controle de
                    animais, pastos e fazendas. Os usuários podem acessar o sistema por meio de uma interface web
                    simples e intuitiva, que permite o gerenciamento de dados e a visualização de relatórios.</p>
                <p>Os fazendeiros podem cadastrar seus animais, pastos e fazendas no sistema, incluindo informações como
                    nome, descrição e localização. O FarmNote permite o controle de dados como peso dos bovinos, status
                    dos pastos (ocupado, em recuperação, livre), e informações sobre as fazendas.</p>
                <p>Os usuários também podem programar atividades relacionadas ao gado, como alimentação, vacinação e
                    reprodução, e visualizar relatórios e gráficos que mostram informações importantes sobre seus
                    animais, pastos e fazendas.</p>
            </section>
